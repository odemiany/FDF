#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	char *dst2;

	dst2 = dst;
	while ((*src != '\0') && (len > 0))
	{
		*dst = *src;
		dst++;
		src++;
		len--;
	}
	while (len > 0)
	{
		*dst = '\0';
		dst++;
		len--;
	}
	return (dst2);
}
